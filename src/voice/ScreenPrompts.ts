// Hook for using screen voice prompts
export function useScreenVoicePrompt(
  screenKey: string, 
  variables?: Record<string, string | number>,
  language: Language = 'hi',
  autoRead: boolean = true,
  ttsSpeak?: (text: string) => void
) {
  if (!autoRead || !ttsSpeak) return;
  
  const template = SCREEN_PROMPTS[screenKey]?.[language] ?? SCREEN_PROMPTS[screenKey]?.['hi'];
  if (!template) return;

  const text = template.replace(/\{(\w+)\}/g, (_, key) => String(variables?.[key] ?? ''));
  
  // 500ms delay after mount
  setTimeout(() => ttsSpeak(text), 500);
}
